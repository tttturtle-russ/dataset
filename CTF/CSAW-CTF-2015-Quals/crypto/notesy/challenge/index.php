<script>
$('#note_data').bind('input propertychange', function() {
    var text = $('#note_data').val();
    if (text.length == 0){
        $('#note_error').hide();
    }
    else if (text.length < 5){
        $('#note_error').show();
        $('#note_output').hide();
    }

    else{
        $('#note_error').hide();
        $('#note_output').show();
        $.get('encrypt.php', {m:text}, function(d){
            $('#note_output').text(d);
        });
    }
});
